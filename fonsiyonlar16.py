def factoriel(numara):
    faktoriyel=1
    for i in range(1,numara+1):  
        faktoriyel*=i    #burada faktöriyeli hesaplattım.
    print("faktöriyeel",faktoriyel)   #burada ekrana yazılmasını sağladık.
        
        
sayi=int(input("sayı giriniz: "))
factoriel(sayi)  # fonksiyonu cagırdım bu şekilde ve faktoriyeli basacaktıt.

factoriel(2)  #bu şekildede yazabilirim kllanıcı girmeden
factoriel(3)
factoriel(4)
